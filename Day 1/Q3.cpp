void count(vector<int> a,int n)
{
vector<int> b;
   if(n==0)
        return  ;
   else
   {
   int k=0,j=n/2;
       for(int i=0;i<n/2;i++)
       {
       b.push_back(a[k+i]);
       b.push_back(a[j+i]);
       }
       for(int i=0;i<n;i++)
        cout<<b[i];
   }
}
